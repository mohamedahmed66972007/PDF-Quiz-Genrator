function applyTheme(theme: AppTheme) {
  const root = document.documentElement;
  const vars = COLOR_VARS[theme.color][theme.mode];
  for (const [k, v] of Object.entries(vars)) {
    root.style.setProperty(k, v);
  }
  if (theme.mode === "dark") {
    root.classList.add("dark");
  } else {
    root.classList.remove("dark");
  }
}

export function ThemeProvider({ children }: { children: React.ReactNode }) {
  const [theme, setTheme] = useState<AppTheme>(() => {
    const saved = loadTheme();
    return saved ?? DEFAULT_THEME;
  });

  useEffect(() => {
    applyTheme(theme);
    saveTheme(theme);
  }, [theme]);

  // Auto-change color on each session if enabled
  useEffect(() => {
    if (theme.autoChange) {
      const randomColor = COLORS[Math.floor(Math.random() * COLORS.length)];
      setTheme((t) => ({ ...t, color: randomColor }));
    }
  }, []); // eslint-disable-line react-hooks/exhaustive-deps

  const setMode = (mode: ThemeMode) => setTheme((t) => ({ ...t, mode }));
  const setColor = (color: ThemeColor) => setTheme((t) => ({ ...t, color }));
  const toggleMode = () =>
    setTheme((t) => ({ ...t, mode: t.mode === "light" ? "dark" : "light" }));
  const setAutoChange = (autoChange: boolean) =>
    setTheme((t) => ({ ...t, autoChange }));

  return (
    <ThemeContext.Provider
      value={{ theme, setMode, setColor, toggleMode, setAutoChange }}
    >
      {children}
    </ThemeContext.Provider>
  );
}

export function useTheme(): ThemeContextValue {
  const ctx = useContext(ThemeContext);
  if (!ctx) throw new Error("useTheme must be used inside ThemeProvider");
  return ctx;
}

